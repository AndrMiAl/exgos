function pickBestGuideForOption(guides: ConceptGuide[], optionText: string, correctGuideKeys: Set<string>) {
  const normalizedOption = normalizeOptionToken(optionText)
  const nonCorrectGuides = guides.filter((guide) => !correctGuideKeys.has(guide.key))
  const candidates = nonCorrectGuides.length > 0 ? nonCorrectGuides : guides

  return (
    candidates
      .map((guide) => {
        const normalizedTitle = normalizeOptionToken(guide.title)
        const exactAliasMatch = guide.aliases.some(
          (alias) => normalizeOptionToken(alias) === normalizedOption && normalizedOption.length > 0,
        )
        const titleMatch =
          normalizedTitle.length > 0 &&
          (normalizedOption.includes(normalizedTitle) || normalizedTitle.includes(normalizedOption))
        const aliasContainsMatch = guide.aliases.some((alias) => {
          const normalizedAlias = normalizeOptionToken(alias)
          return normalizedAlias.length > 0 && normalizedOption.includes(normalizedAlias)
        })

        return {
          guide,
          score:
            (exactAliasMatch ? 100 : 0) +
            (titleMatch ? 60 : 0) +
            (aliasContainsMatch ? 30 : 0) +
            Math.max(...guide.aliases.map((alias) => alias.length)),
        }
      })
      .sort((left, right) => right.score - left.score)[0]?.guide ?? null
  )
}

function buildWrongOptionInsight(
  optionText: string,
  shortExplanation: string,
  correctGuideKeys: Set<string>,
  correctGuides: ConceptGuide[],
) {
  const cleanedOption = stripTrailingReason(optionText)
  const explicitReason = extractTrailingReason(optionText)
  const wrongOptionGuides = findOptionGuides(cleanedOption)
  const mainWrongGuide = pickBestGuideForOption(wrongOptionGuides, cleanedOption, correctGuideKeys)
  const mainCorrectGuide = correctGuides[0] ?? null
  const conciseShortExplanation = lowerFirst(stripTrailingReason(shortExplanation))
  const generatorQuestion =
    correctGuideKeys.has('yield') ||
    correctGuideKeys.has('generator') ||
    correctGuideKeys.has('generator-expression') ||
    normalizeText(shortExplanation).includes('генератор')
  const pushQuestion = correctGuideKeys.has('js-array-push')
  const htmlStructureQuestion =
    mainWrongGuide !== null &&
    mainCorrectGuide !== null &&
    ['html-head', 'html-body', 'html-footer', 'html-title'].includes(mainWrongGuide.key) &&
    ['html-head', 'html-body', 'html-footer', 'html-title'].includes(mainCorrectGuide.key)

  if (pushQuestion && mainWrongGuide) {
    if (mainWrongGuide.key === 'js-array-concat') {
      return `${cleanedOption} не подходит. \`concat()\` создает новый массив на основе старого и возвращает этот новый результат. Он нужен, когда вы хотите получить объединенный массив, а не просто дописать элемент в конец текущего массива на месте.`
    }

    if (mainWrongGuide.key === 'js-array-join') {
      return `${cleanedOption} не подходит. \`join()\` вообще не добавляет элемент в массив. Он берет уже существующие элементы массива и склеивает их в одну строку через разделитель, поэтому результат тут будет строкой, а не обновленным массивом.`
    }

    if (mainWrongGuide.key === 'dom-append-child') {
      return `${cleanedOption} не подходит. \`appendChild()\` относится к HTML-элементам на странице, а не к массивам JavaScript. Этим методом вставляют DOM-узел внутрь другого DOM-узла, например новый \`li\` внутрь \`ul\`.`
    }
  }

  if (generatorQuestion && mainWrongGuide) {
    if (mainWrongGuide.key === 'python-list' || mainWrongGuide.key === 'list-comprehension') {
      return `${cleanedOption} не подходит. Это описание списка: список обычно создается целиком и хранит все элементы сразу. Генератор работает иначе: он выдает следующее значение только в момент запроса и не обязан держать всю коллекцию в памяти.`
    }

    if (mainWrongGuide.key === 'import') {
      return `${cleanedOption} не подходит. \`import\` нужен для подключения модуля или имени из другого файла. К генераторам это не относится: здесь речь о функции, которая выдает значения постепенно и запоминает свое состояние.`
    }

    if (mainWrongGuide.key === 'python-class') {
      return `${cleanedOption} не подходит. Класс объявляет новый тип объектов, а не механизм выдачи значений. В этом вопросе речь о генераторе: функция с \`yield\` отдает значения по одному и может продолжить работу позже.`
    }

    if (mainWrongGuide.key === 'return') {
      return `${cleanedOption} не подходит. \`return\` завершает функцию окончательно. \`yield\` ведет себя иначе: он отдает текущее значение, приостанавливает функцию и позволяет продолжить выполнение с того же места при следующем обращении.`
    }

    if (mainWrongGuide.key === 'async-await') {
      return `${cleanedOption} не подходит. \`await\` и корутины относятся к асинхронному коду, когда нужно дождаться завершения операции. Здесь спрашивают про генератор, который нужен не для ожидания, а для пошаговой выдачи значений.`
    }
  }

  if (htmlStructureQuestion && mainWrongGuide && mainCorrectGuide) {
    if (mainCorrectGuide.key === 'html-head') {
      if (mainWrongGuide.key === 'html-body') {
        return `${cleanedOption} не подходит. \`body\` — это видимая часть страницы: текст, кнопки, картинки и остальной контент, который видит человек. Но здесь спрашивают не про содержимое страницы, а про служебную часть документа, где лежат метаданные. Именно поэтому \`title\` размещают в \`head\`, а не в \`body\`.`
      }

      if (mainWrongGuide.key === 'html-footer') {
        return `${cleanedOption} не подходит. \`footer\` — это нижний блок самой страницы, например с копирайтом или контактами. Он относится к содержимому сайта внутри \`body\`. А \`title\` — это служебный элемент документа для вкладки браузера, поэтому его место в \`head\`.`
      }

      if (mainWrongGuide.key === 'html-title') {
        return `${cleanedOption} не подходит. \`title\` — это сам элемент с названием вкладки, а в вопросе спрашивают, в какой секции документа он должен находиться. Этот элемент не заменяет \`head\`, а живет внутри него.`
      }
    }

    if (mainCorrectGuide.key === 'html-body') {
      if (mainWrongGuide.key === 'html-head') {
        return `${cleanedOption} не подходит. \`head\` хранит служебную информацию о странице: title, meta-теги, подключения стилей и другую информацию для браузера. Основной видимый контент страницы находится не там, а в \`body\`, потому что именно \`body\` браузер показывает пользователю как саму страницу.`
      }

      if (mainWrongGuide.key === 'html-footer') {
        return `${cleanedOption} не подходит. \`footer\` — это только нижняя часть страницы, а не вся страница целиком. Вопрос спрашивает про основной видимый контент документа, и для этого используется весь \`body\`, внутри которого уже может находиться \`footer\`.`
      }

      if (mainWrongGuide.key === 'html-title') {
        return `${cleanedOption} не подходит. \`title\` задает название вкладки браузера и относится к метаданным документа. Он не хранит основной текст, кнопки, формы и другие видимые элементы страницы. Все это размещают в \`body\`.`
      }
    }

    if (mainCorrectGuide.key === 'html-title') {
      if (mainWrongGuide.key === 'html-head') {
        return `${cleanedOption} не подходит. \`head\` — это большая служебная секция документа, внутри которой лежат разные метаданные. Но в вопросе спрашивают именно про элемент, который задает название вкладки браузера. Это делает не весь \`head\`, а конкретно тег \`title\`.`
      }

      if (mainWrongGuide.key === 'html-body') {
        return `${cleanedOption} не подходит. \`body\` отвечает за видимое содержимое страницы, которое читает пользователь. Название вкладки браузера относится не к контенту страницы, а к метаданным документа, поэтому за него отвечает тег \`title\`.`
      }
    }
  }

  if (mainWrongGuide && !correctGuideKeys.has(mainWrongGuide.key)) {
    const wrongMeaning = getGuideMeaningText(mainWrongGuide)

    if (mainCorrectGuide) {
      return `${cleanedOption} не подходит. Этот вариант означает следующее: ${wrongMeaning}. А здесь нужно другое: ${getGuideMeaningText(mainCorrectGuide)}.`
    }

    return `${cleanedOption} не подходит. Этот вариант описывает ${wrongMeaning}, а в вопросе спрашивается другое.`
  }

  if (explicitReason) {
    if (mainCorrectGuide) {
      return `${cleanedOption} не подходит. Здесь перепутан смысл варианта: ${upperFirst(lowerFirst(explicitReason))}. Для этого вопроса нужен другой механизм: ${getGuideMeaningText(mainCorrectGuide)}.`
    }

    return `${cleanedOption} не подходит. Здесь перепутан смысл варианта: ${upperFirst(lowerFirst(explicitReason))}.`
  }

  return `${cleanedOption} не подходит: здесь верно следующее — ${conciseShortExplanation}.`
}

export function buildConceptExplanation(input: BuildConceptExplanationInput): StudyConceptExplanation {
  const {
    questionText,
    concept,
    shortExplanation,
    detailedExplanation,
    correctOptionText = '',
    wrongOptionTexts,
  } = input

  const questionKind = getQuestionKind(questionText)
  const matchedGuides = findMatchedGuides(
    questionText,
    [correctOptionText],
    [shortExplanation, detailedExplanation],
  )
  const pairGuide = findPairGuide(matchedGuides)
  const focusedGuides = matchedGuides.slice(0, 3)
  const fallbackPlainLanguage = buildFallbackPlainLanguage(questionKind, concept, correctOptionText, shortExplanation)
  const fallbackDetailed = buildFallbackDetailed(
    questionKind,
    shortExplanation,
    detailedExplanation,
    correctOptionText,
  )
  const plainLanguage = pairGuide
    ? pairGuide.plainLanguage
    : focusedGuides.length > 0
      ? uniqueTrimmed([
          shortExplanation ? `Главная идея вопроса такая: ${shortExplanation}` : '',
          focusedGuides.length === 1 ? 'Если разложить по термину:' : 'Если разложить по терминам:',
          ...focusedGuides.map((guide) => `${guide.title} — ${upperFirst(lowerFirst(guide.summary))}`),
        ]).join('\n')
      : fallbackPlainLanguage
  const detailed = pairGuide
    ? `${pairGuide.detailed} Правильный вариант: ${stripTrailingReason(correctOptionText || shortExplanation)}.`
    : focusedGuides.length > 0
      ? uniqueTrimmed([
          correctOptionText ? `Правильный вариант: ${stripTrailingReason(correctOptionText)}.` : '',
          shortExplanation ? `Почему именно он: ${shortExplanation}` : '',
          upperFirst(detailedExplanation),
        ]).join(' ')
      : fallbackDetailed
  const usageText = pairGuide
    ? pairGuide.useCases
    : focusedGuides.length > 0
      ? focusedGuides.map((guide) => guide.useCases).join('\n')
      : buildFallbackUsageText(questionKind, shortExplanation)
  const purposeText = pairGuide
    ? pairGuide.whyItMatters
    : focusedGuides.length > 0
      ? focusedGuides.map((guide) => guide.whyItMatters).join(' ')
      : buildFallbackPurposeText(questionKind, concept)
  const correctGuideKeys = new Set(matchedGuides.map((guide) => guide.key))
  const alternatives = uniqueTrimmed(
    wrongOptionTexts.map((optionText) =>
      buildWrongOptionInsight(optionText, shortExplanation, correctGuideKeys, focusedGuides),
    ),
  )
  const confusions = uniqueTrimmed([
    ...(pairGuide?.mixups ?? []),
    ...focusedGuides.flatMap((guide) => guide.mixups),
    ...wrongOptionTexts,
  ]).slice(0, 5)

  return {
    plainLanguage,
    detailed,
    usageText,
    purposeText,
    conceptCards: buildConceptCards(focusedGuides),
    relatedCards: buildRelatedCards(focusedGuides),
    alternatives,
    confusions,
  }
}
