function normalizeText(text: string) {
  return text.toLowerCase().replace(/\s+/g, ' ').trim()
}

function escapeRegExp(text: string) {
  return text.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

function uniqueTrimmed(values: string[]) {
  return [...new Set(values.map((value) => value.trim()).filter(Boolean))]
}

function stripTrailingReason(text: string) {
  return text
    .replace(/,\s*потому что.+$/i, '')
    .replace(/\.\s*$/g, '')
    .trim()
}

function extractTrailingReason(text: string) {
  const match = text.match(/,\s*потому что\s+(.+)$/i)

  if (!match) {
    return ''
  }

  return match[1].replace(/\.\s*$/g, '').trim()
}

function lowerFirst(text: string) {
  if (!text) {
    return text
  }

  return text.charAt(0).toLowerCase() + text.slice(1)
}

function upperFirst(text: string) {
  if (!text) {
    return text
  }

  return text.charAt(0).toUpperCase() + text.slice(1)
}

function normalizeOptionToken(text: string) {
  return normalizeText(text).replace(/[`"'«»]/g, '').trim()
}

function hasAlias(text: string, alias: string) {
  const normalizedText = normalizeText(text)
  const normalizedAlias = normalizeText(alias)

  if (!normalizedAlias) {
    return false
  }

  if (/^[a-zа-яё0-9_-]+$/i.test(normalizedAlias)) {
    const pattern = new RegExp(`(^|[^a-zа-яё0-9_])${escapeRegExp(normalizedAlias)}($|[^a-zа-яё0-9_])`, 'i')
    return pattern.test(normalizedText)
  }

  return normalizedText.includes(normalizedAlias)
}

function guideMatches(guide: ConceptGuide, combinedText: string) {
  const hasMainAlias = guide.aliases.some((alias) => hasAlias(combinedText, alias))

  if (!hasMainAlias) {
    return false
  }

  if (guide.requiresAny && !guide.requiresAny.some((alias) => hasAlias(combinedText, alias))) {
    return false
  }

  if (guide.excludesAny && guide.excludesAny.some((alias) => hasAlias(combinedText, alias))) {
    return false
  }

  return true
}

function getFirstAliasPosition(text: string, aliases: string[]) {
  const normalizedText = normalizeText(text)
  const positions = aliases
    .map((alias) => normalizedText.indexOf(normalizeText(alias)))
    .filter((position) => position >= 0)

  return positions.length ? Math.min(...positions) : Number.MAX_SAFE_INTEGER
}

function findMatchedGuides(questionText: string, preferredTexts: string[], contextTexts: string[]) {
  const combinedText = [questionText, ...preferredTexts, ...contextTexts].join(' ')
  const normalizedQuestion = normalizeText(questionText)
  const normalizedPreferred = normalizeText(preferredTexts.join(' '))

  return conceptGuides
    .filter((guide) => guideMatches(guide, combinedText))
    .map((guide) => {
      return {
        guide,
        questionPosition: getFirstAliasPosition(normalizedQuestion, guide.aliases),
        preferredPosition: getFirstAliasPosition(normalizedPreferred, guide.aliases),
        aliasLength: Math.max(...guide.aliases.map((alias) => alias.length)),
      }
    })
    .sort((left, right) => {
      if (left.questionPosition !== right.questionPosition) {
        return left.questionPosition - right.questionPosition
      }

      if (left.preferredPosition !== right.preferredPosition) {
        return left.preferredPosition - right.preferredPosition
      }

      return right.aliasLength - left.aliasLength
    })
    .map((entry) => entry.guide)
}

function findOptionGuides(optionText: string) {
  const normalizedOption = normalizeText(optionText)

  return conceptGuides
    .filter((guide) => guide.aliases.some((alias) => hasAlias(optionText, alias)))
    .map((guide) => {
      const exactAliasMatch = guide.aliases.some(
        (alias) => normalizeOptionToken(alias) === normalizeOptionToken(optionText) && normalizedOption.length > 0,
      )

      return {
        guide,
        position: getFirstAliasPosition(normalizedOption, guide.aliases),
        aliasLength: Math.max(...guide.aliases.map((alias) => alias.length)),
        exactAliasMatch,
      }
    })
    .sort((left, right) => {
      if (left.exactAliasMatch !== right.exactAliasMatch) {
        return Number(right.exactAliasMatch) - Number(left.exactAliasMatch)
      }

      if (left.position !== right.position) {
        return left.position - right.position
      }

      return right.aliasLength - left.aliasLength
    })
    .map((entry) => entry.guide)
}

function getQuestionKind(questionText: string) {
  const normalized = normalizeText(questionText)

  if (normalized.startsWith('что такое')) {
    return 'definition'
  }

  if (normalized.startsWith('чем') || normalized.includes('отличается')) {
    return 'comparison'
  }

  if (normalized.startsWith('что делает') || normalized.startsWith('для чего используется')) {
    return 'usage'
  }

  if (normalized.startsWith('что произойдет')) {
    return 'behavior'
  }

  return 'general'
}

