function normalizeText(text) {
    return text.toLowerCase().replace(/\s+/g, ' ').trim();
}
function escapeRegExp(text) {
    return text.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}
function uniqueTrimmed(values) {
    return [...new Set(values.map((value) => value.trim()).filter(Boolean))];
}
function stripTrailingReason(text) {
    return text
        .replace(/,\s*потому что.+$/i, '')
        .replace(/\.\s*$/g, '')
        .trim();
}
function lowerFirst(text) {
    if (!text) {
        return text;
    }
    return text.charAt(0).toLowerCase() + text.slice(1);
}
function hasAlias(text, alias) {
    const normalizedText = normalizeText(text);
    const normalizedAlias = normalizeText(alias);
    if (!normalizedAlias) {
        return false;
    }
    if (/^[a-zа-яё0-9_-]+$/i.test(normalizedAlias)) {
        const pattern = new RegExp(`(^|[^a-zа-яё0-9_])${escapeRegExp(normalizedAlias)}($|[^a-zа-яё0-9_])`, 'i');
        return pattern.test(normalizedText);
    }
    return normalizedText.includes(normalizedAlias);
}
function guideMatches(guide, combinedText) {
    const hasMainAlias = guide.aliases.some((alias) => hasAlias(combinedText, alias));
    if (!hasMainAlias) {
        return false;
    }
    if (guide.requiresAny && !guide.requiresAny.some((alias) => hasAlias(combinedText, alias))) {
        return false;
    }
    if (guide.excludesAny && guide.excludesAny.some((alias) => hasAlias(combinedText, alias))) {
        return false;
    }
    return true;
}
function getFirstAliasPosition(text, aliases) {
    const normalizedText = normalizeText(text);
    const positions = aliases
        .map((alias) => normalizedText.indexOf(normalizeText(alias)))
        .filter((position) => position >= 0);
    return positions.length ? Math.min(...positions) : Number.MAX_SAFE_INTEGER;
}
function findMatchedGuides(questionText, preferredTexts, contextTexts) {
    const combinedText = [questionText, ...preferredTexts, ...contextTexts].join(' ');
    const normalizedQuestion = normalizeText(questionText);
    const normalizedPreferred = normalizeText(preferredTexts.join(' '));
    return conceptGuides
        .filter((guide) => guideMatches(guide, combinedText))
        .map((guide) => {
        return {
            guide,
            questionPosition: getFirstAliasPosition(normalizedQuestion, guide.aliases),
            preferredPosition: getFirstAliasPosition(normalizedPreferred, guide.aliases),
            aliasLength: Math.max(...guide.aliases.map((alias) => alias.length)),
        };
    })
        .sort((left, right) => {
        if (left.questionPosition !== right.questionPosition) {
            return left.questionPosition - right.questionPosition;
        }
        if (left.preferredPosition !== right.preferredPosition) {
            return left.preferredPosition - right.preferredPosition;
        }
        return right.aliasLength - left.aliasLength;
    })
        .map((entry) => entry.guide);
}
function getQuestionKind(questionText) {
    const normalized = normalizeText(questionText);
    if (normalized.startsWith('что такое')) {
        return 'definition';
    }
    if (normalized.startsWith('чем') || normalized.includes('отличается')) {
        return 'comparison';
    }
    if (normalized.startsWith('что делает') || normalized.startsWith('для чего используется')) {
        return 'usage';
    }
    if (normalized.startsWith('что произойдет')) {
        return 'behavior';
    }
    return 'general';
}
function findPairGuide(guides) {
    const keys = new Set(guides.map((guide) => guide.key));
    return pairGuides.find((pairGuide) => pairGuide.keys.every((key) => keys.has(key))) ?? null;
}
function buildFallbackPlainLanguage(questionKind, concept, correctOptionText, shortExplanation) {
    const answerText = stripTrailingReason(correctOptionText || shortExplanation);
    if (questionKind === 'definition') {
        return `${concept} — ${lowerFirst(answerText)}.`;
    }
    if (questionKind === 'comparison') {
        return `${concept}: главное различие здесь такое — ${lowerFirst(answerText)}.`;
    }
    if (questionKind === 'usage') {
        return `${concept} используют так: ${lowerFirst(answerText)}.`;
    }
    if (questionKind === 'behavior') {
        return `В этом вопросе про ${concept} проверяется такое поведение: ${lowerFirst(answerText)}.`;
    }
    return `${concept} связан с такой идеей: ${lowerFirst(answerText)}.`;
}
function buildFallbackDetailed(questionKind, concept, shortExplanation, detailedExplanation, correctOptionText) {
    const answerText = stripTrailingReason(correctOptionText || shortExplanation);
    const details = uniqueTrimmed([
        questionKind === 'comparison'
            ? `Правильный ответ держится на сравнении понятий: ${concept}.`
            : `Правильный ответ опирается на понимание того, что означает ${concept}.`,
        answerText ? `Ключевая мысль здесь: ${answerText}.` : '',
        detailedExplanation,
    ]);
    return details.join(' ');
}
function buildFallbackUsageText(questionKind, concept, shortExplanation) {
    if (questionKind === 'comparison') {
        return `Такие сравнения нужны на практике, когда важно выбрать подходящий инструмент из близких вариантов и не перепутать их свойства. ${concept} лучше понимать не по названию, а по реальным ограничениям и возможностям.`;
    }
    return `${concept} нужно не только помнить по определению, но и узнавать в коде, настройках или формулировках похожих задач. ${shortExplanation}`;
}
function buildFallbackPurposeText(questionKind, concept) {
    if (questionKind === 'definition') {
        return `Такой вопрос проверяет, можете ли вы своими словами объяснить, что такое ${concept}, а не только узнать знакомый термин.`;
    }
    if (questionKind === 'comparison') {
        return `Такой вопрос проверяет, видите ли вы принципиальную разницу между близкими понятиями и понимаете ли, почему эта разница важна в коде и задачах.`;
    }
    if (questionKind === 'usage') {
        return `Такой вопрос проверяет практическое понимание: когда и зачем нужен ${concept}, а не только формальное определение.`;
    }
    if (questionKind === 'behavior') {
        return `Такой вопрос проверяет, понимаете ли вы поведение ${concept} в конкретной ситуации, а не угадываете ответ по ключевым словам.`;
    }
    return `Такой вопрос проверяет, умеете ли вы применить идею ${concept} в близких формулировках, а не запомнить один частный ответ.`;
}
function buildConceptCards(guides) {
    if (guides.length > 0) {
        return guides.slice(0, 3).map((guide) => ({
            title: guide.title,
            summary: guide.summary,
            details: guide.useCases,
        }));
    }
    return [];
}
export function buildConceptExplanation(input) {
    const { questionText, concept, shortExplanation, detailedExplanation, correctOptionText = '', wrongOptionTexts, } = input;
    const questionKind = getQuestionKind(questionText);
    const matchedGuides = findMatchedGuides(questionText, [correctOptionText], [shortExplanation, detailedExplanation, ...wrongOptionTexts]);
    const pairGuide = findPairGuide(matchedGuides);
    const focusedGuides = matchedGuides.slice(0, 3);
    const fallbackPlainLanguage = buildFallbackPlainLanguage(questionKind, concept, correctOptionText, shortExplanation);
    const fallbackDetailed = buildFallbackDetailed(questionKind, concept, shortExplanation, detailedExplanation, correctOptionText);
    const plainLanguage = pairGuide
        ? pairGuide.plainLanguage
        : focusedGuides.length > 0
            ? `${focusedGuides.length === 1 ? 'Сначала разберем термин: ' : 'Сначала разберем термины: '}${focusedGuides
                .map((guide) => `\`${guide.title}\` — ${lowerFirst(guide.summary)}`)
                .join(' ')}`
            : fallbackPlainLanguage;
    const detailed = pairGuide
        ? `${pairGuide.detailed} Теперь к самому ответу: ${stripTrailingReason(correctOptionText || shortExplanation)}.`
        : focusedGuides.length > 0
            ? uniqueTrimmed([
                correctOptionText ? `Теперь к самому ответу: ${stripTrailingReason(correctOptionText)}.` : '',
                shortExplanation ? `Почему это верно: ${shortExplanation}` : '',
                detailedExplanation,
            ]).join(' ')
            : fallbackDetailed;
    const usageText = pairGuide
        ? pairGuide.useCases
        : focusedGuides.length > 0
            ? focusedGuides.map((guide) => guide.useCases).join(' ')
            : buildFallbackUsageText(questionKind, concept, shortExplanation);
    const purposeText = pairGuide
        ? pairGuide.whyItMatters
        : focusedGuides.length > 0
            ? focusedGuides.map((guide) => guide.whyItMatters).join(' ')
            : buildFallbackPurposeText(questionKind, concept);
    const confusions = uniqueTrimmed([
        ...(pairGuide?.mixups ?? []),
        ...focusedGuides.flatMap((guide) => guide.mixups),
        ...wrongOptionTexts,
    ]).slice(0, 5);
    return {
        plainLanguage,
        detailed,
        usageText,
        purposeText,
        conceptCards: buildConceptCards(focusedGuides),
        confusions,
    };
}
